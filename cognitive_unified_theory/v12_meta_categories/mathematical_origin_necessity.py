        E = np.array([[1, 0], [0, 0]], dtype=np.complex128)  # 存在投影
        delta_star = math.sqrt(self.beta / self.gamma)
        G = self.c * self.I2 + delta_star * self.sigma_x  # 度规演化

        # 非交换性
        commutator = E @ G - G @ E
        noncomm_measure = float(np.linalg.norm(commutator))

        # 验证：非交换合成 → *-代数
        # M₂(ℂ) 是 *-代数（对合 = 共轭转置）
